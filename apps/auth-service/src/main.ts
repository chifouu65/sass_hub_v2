/**
 * This is not a production server yet!
 * This is only a minimal backend to get started.
 */

import { Logger, ValidationPipe } from '@nestjs/common';
import { NestFactory } from '@nestjs/core';
import { AppModule } from './app/app.module';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);
  const globalPrefix = 'api';
  app.setGlobalPrefix(globalPrefix);

  app.enableCors({
    origin: process.env.FRONTEND_URL || 'http://localhost:4200',
    credentials: true,
  });

  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  );

  const port = Number(
    process.env.AUTH_SERVICE_PORT ?? process.env.PORT ?? 3001,
  );
  await app.listen(port);
  Logger.log(
    `ðŸš€ Auth service running at http://localhost:${port}/${globalPrefix}`,
  );
  Logger.log(`   Health check: http://localhost:${port}/${globalPrefix}/health`);
  Logger.log(`   Auth endpoints: http://localhost:${port}/${globalPrefix}/auth`);
}

bootstrap();
